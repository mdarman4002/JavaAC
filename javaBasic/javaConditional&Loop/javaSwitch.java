public class javaSwitch {
    public static void main(String args[]){

        int num = 4;

        switch(num){
            case 1: System.out.println("mobile");
            break;
        case 2: System.out.println("charger");
            break;
        case 3: System.out.println("laptop");
            break;
        case 4: System.out.println("bag");
            break;
        default:
            System.out.println("Default");
        }
        
    }
}
